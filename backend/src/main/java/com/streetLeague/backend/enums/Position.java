package com.streetLeague.backend.enums;

/**
 * Enum for player positions on the field.
 * 
 * Defines different tactical positions a player can play:
 * - GOALKEEPER: Defends the goal
 * - DEFENDER: Defensive player (back line)
 * - MIDFIELDER: Mid-field player (center)
 * - FORWARD: Attacking player (front line)
 * - UTILITY: Multi-purpose player
 * 
 * @author StreetLeague Team
 * @version 1.0
 */
public enum Position {
    GOALKEEPER,
    DEFENDER,
    MIDFIELDER,
    FORWARD,
    UTILITY
}
